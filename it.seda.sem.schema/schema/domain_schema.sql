		id bigint generated by default as identity(start with 0),
		NOME VARCHAR(50) NOT NULL,
		DESCRIZIONE VARCHAR(256) NOT NULL,
		REGISTRAZIONE TIMESTAMP NOT NULL,
	);                   
		                 
